package com.Xworkz.controlStatement;

public class SumOfNumbers {

	public static void main(String[] args) {
		
		int i = 0;    
		System.out.println("Printing the list of first 10 even numbers \n");    
		do {    
			i = i + 2; 
		
		System.out.println(i);    
		   
		}while(i<=10);

	}

}
